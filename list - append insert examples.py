# 3.1.4.8 Lists - collections of data | list methods

# append = menambahkan item kedalam list setelah elemen terakhir

print("\nlist original")

a = [1,2,3,4]

print(a, "panjang list : ",len(a))

# append list a dengan number 5

a.append(5)

print("\nlist yang di append")

print(a, "panjang list : ",len(a))

# insert = menambahkan item ke dalam list dengan posisi tertentu

a.insert(0,0)

print("\nlist yang di insert")

print(a, "panjang list : ",len(a))