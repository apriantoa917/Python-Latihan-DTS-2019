# 5.1.8.14 The nature of strings in Python

# Demonstrating the list() function
print(list("abcabc"))

# list : mengkonversi sebuah string menjadi setiap elemen di dalam list

# Demonstrating the count() method
print("abcabc".count("b"))
print('abcabc'.count("d"))

# count : menghitung jumlah sebuah karakter yang sama dalam suatu kata