# 5.1.9.1 String methods

#capitalize() = menjadikan sebuah kata menjadi kapital(hanya huruf depan kata pertama yang besar)

print("Alpha".capitalize())
print('ALPHA'.capitalize())
print(' Alpha'.capitalize())
print('123'.capitalize())
print("αβγδ".capitalize())
print("alpha beta teta".capitalize())

# lower() = menjadikan sebuah kata menjadi huruf kecil semua

print("SiGmA=60".lower())
print("SisMM".lower())
print("SAMA = sama".lower())
print("saMA= SAma".lower())
